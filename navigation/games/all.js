// This function creates the HTML for each game
function createGameHTML({ title, url, img, label, mode }) {
  return `
    <div class="game-item">
      <button 
        onclick="handleGameClick(this)" 
        class="game-button" 
        data-url="${url}" 
        data-mode="${mode}" 
        aria-label="${label}">
        <img src="${img}" alt="${label} Game Button Image">
      </button>
      <p class="game-title">${title}</p>
    </div>
  `;
}

// Inject the game buttons into the container
document.getElementById('gameContainer').innerHTML = games.map(createGameHTML).join('');

// Handle button click based on mode
function handleGameClick(element) {
  const url = element.getAttribute('data-url');
  const mode = element.getAttribute('data-mode');

  if (mode === 'blob') {
    loadBlobContent(url); // Load via AJAX or iframe
  } else if (mode === 'page') {
    window.location.href = url; // Navigate to full page
  } else {
    console.warn('Unknown mode:', mode);
  }
}